# Create input fields dynamically based on the dataset features
user_input = {}
for feature in dt.columns[:-1]:
    label_text = feature_labels.get(feature, feature)
    if feature == 'Gender':
        gender_options = {'‡∏ä‡∏≤‡∏¢': 0, '‡∏´‡∏ç‡∏¥‡∏á': 1}
